    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        // TODO add your handling code here:
        DTOPhieuNhap pn = BLLpn.BLLtim(textSearch.getText().trim());
        if(pn != null){
            model = new DefaultTableModel();
            model.addColumn("Mã PN");
            model.addColumn("Giờ nhập");
            model.addColumn("Ngày nhập");
            model.addColumn("Tổng tiền");
            model.addColumn("Mã NCC");
            model.addColumn("Mã NV");
            tablePN.setModel(model);
            Object[] row = {pn.getMaPhieuNhap(),pn.getGioNhap(),pn.getNgayNhap(),pn.getTongTien(),pn.getMaNCC(),pn.getMaNV()};
            model.addRow(row);
        }else{
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã phiếu nhập");
        }
    }//GEN-LAST:event_buttonSearchActionPerformed

