    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        try {
            if(textMaPN.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mời bạn nhập mã phiếu nhập!");
            }
            else if(textGioNhap.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập giờ!");
            }
            else if(textNgayNhap.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập ngày!");
            }
            else if(textTongTien.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập tổng tiền!");
            }
            else if(textMaNCC.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập mã nhà cung cấp!");
            }
            else if(textMaNV.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập mã nhân viên!");
            }
            else{
                DTOPhieuNhap pn = new DTOPhieuNhap();
                pn.setMaPhieuNhap(textMaPN.getText());
                pn.setGioNhap(Integer.valueOf(textGioNhap.getText()));
                pn.setNgayNhap(textNgayNhap.getText());
                pn.setTongTien(Float.valueOf(textTongTien.getText()));
                pn.setMaNCC(textMaNCC.getText());
                pn.setMaNV(textMaNV.getText());
                JOptionPane.showMessageDialog(this,BLLpn.BLLthem(pn));
                loadPNList();
                textMaNCC.setText("");textMaPN.setText("");textMaNV.setText("");textGioNhap.setText("");textNgayNhap.setText("");textTongTien.setText("");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_buttonAddActionPerformed

