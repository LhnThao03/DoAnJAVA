    public void loadPNList(){
        model = new DefaultTableModel();
        model.addColumn("Mã PN");
        model.addColumn("Giờ nhập");
        model.addColumn("Ngày nhập");
        model.addColumn("Tổng tiền");
        model.addColumn("Mã NCC");
        model.addColumn("Mã NV");
        tablePN.setModel(model);
        ArrayList<DTOPhieuNhap> arr = new ArrayList<DTOPhieuNhap>();
        arr = BLLpn.BLLgetDL();
        for(int i = 0; i < arr.size(); i++){
            DTOPhieuNhap pn = arr.get(i);
            String mapn = pn.getMaPhieuNhap();
            int gioNhap = pn.getGioNhap();
            String ngayNhap = pn.getNgayNhap();
            Float tongTien = pn.getTongTien();
            String mancc = pn.getMaNCC();
            String manv = pn.getMaNV();
            Object[] row = {mapn,gioNhap,ngayNhap,tongTien,mancc,manv};
            model.addRow(row);
        }
    }
