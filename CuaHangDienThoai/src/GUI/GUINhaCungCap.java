    public void loadNCCList(){
        model = new DefaultTableModel();
        model.addColumn("Tên NCC");
        model.addColumn("Mã NCC");
        model.addColumn("Địa chỉ");
        model.addColumn("SĐT");
        TableNCC.setModel(model);
        ArrayList<DTONhaCungCap> arr = new ArrayList<DTONhaCungCap>();
        arr = BLLncc.BLLgetDL();
        for(int i = 0; i < arr.size(); i++){
            DTONhaCungCap ncc = arr.get(i);
            String mancc = ncc.getMaNCC();
            String tenncc = ncc.getTenNCC();
            String address = ncc.getDiaChi();
            String sdt = ncc.getSDT();
            Object[] row = {mancc,tenncc,address,sdt};
            model.addRow(row);
        }
    }
    
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if(textMaNCC.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mời bạn nhập mã nhà cung cấp!");
        }
        else if(textTenNCC.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mời bạn nhập tên nhà cung cấp!");
        }
        else if(textDiaChi.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mời bạn nhập địa chỉ!");
        }
        else if(textSDT.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mời bạn nhập số điện thoại nhà cung cấp!");
        }
        else{
            DTONhaCungCap ncc = new DTONhaCungCap();
            ncc.setMaNCC(textMaNCC.getText());
            ncc.setTenNCC(textTenNCC.getText());
            ncc.setDiaChi(textDiaChi.getText());
            ncc.setSDT(textSDT.getText());
            JOptionPane.showMessageDialog(this,BLLncc.BLLthem(ncc));
            loadNCCList();
            textMaNCC.setText("");
            textTenNCC.setText("");
            textDiaChi.setText("");
            textSDT.setText("");
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        // TODO add your handling code here:
        int i=TableNCC.getSelectedRow();
        DTONhaCungCap ncc = new DTONhaCungCap();
        if(i>=0){ 
            ncc.setMaNCC(model.getValueAt(i, 0).toString()); 
            ncc.setTenNCC(model.getValueAt(i, 1).toString());
            ncc.setDiaChi(model.getValueAt(i, 2).toString());
            ncc.setSDT(model.getValueAt(i, 3).toString());
        }
        JOptionPane.showMessageDialog(this,BLLncc.BLLxoa(ncc));
        loadNCCList();
        textMaNCC.setText("");
        textTenNCC.setText("");
        textDiaChi.setText("");
        textSDT.setText("");
    }//GEN-LAST:event_delButtonActionPerformed

