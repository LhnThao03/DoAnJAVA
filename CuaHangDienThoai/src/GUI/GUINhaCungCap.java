    private void buttonTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTimActionPerformed
        // TODO add your handling code here:
        DTONhaCungCap ncc = BLLncc.BLLtim(textSearchMa.getText().trim());
        if(ncc != null){
            model = new DefaultTableModel();
            model.addColumn("Mã NCC");
            model.addColumn("Tên NCC");
            model.addColumn("Địa chỉ");
            model.addColumn("SĐT");
            TableNCC.setModel(model);
            Object[] row = {ncc.getMaNCC(),ncc.getTenNCC(),ncc.getDiaChi(),ncc.getSDT()};
            model.addRow(row);
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã nhà cung cấp");
        }
    }//GEN-LAST:event_buttonTimActionPerformed

