    private void BtnSeachNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSeachNVActionPerformed
        // TODO add your handling code here:
        DTONhanVien nv = BLLnv.BLLtim(txtSearchMaNv.getText().trim());
        if(nv != null){
            model = new DefaultTableModel();
            model.addColumn("Mã NV");
            model.addColumn("Tên NV");
            model.addColumn("Ngày sinh");
            model.addColumn("Địa chỉ");
            model.addColumn("Lương");
            model.addColumn("SĐT");
            model.addColumn("Giới tính");
            tableNV.setModel(model);
            Object[] row = {nv.getMaNV(),nv.getTenNV(),nv.getNgaySinh(),nv.getDiaChi(),nv.getLuong(),nv.getSDT(),nv.getSex()};
            model.addRow(row);
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã nhân viên");
        }
    }//GEN-LAST:event_BtnSeachNVActionPerformed

