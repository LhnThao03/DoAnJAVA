    public void loadNVList(){
        model = new DefaultTableModel();
        model.addColumn("Mã NV");
        model.addColumn("Họ tên");
        model.addColumn("Ngày Sinh");
        model.addColumn("Địa chỉ");
        model.addColumn("Lương");
        model.addColumn("SĐT");
        model.addColumn("Giới tính");
        tableNV.setModel(model);
        ArrayList<DTONhanVien> arr = new ArrayList<DTONhanVien>();
        arr = BLLnv.BLLgetDL();
        for(int i = 0; i < arr.size(); i++){
            DTONhanVien nv = arr.get(i);
            String manv = nv.getMaNV();
            String ten = nv.getTenNV();
            String gioitinh = nv.getSex();
            String ngaysinh = nv.getNgaySinh();
            String diachi = nv.getDiaChi();
            String sdt = nv.getSDT();
            Float luong = nv.getLuong();
            Object[] row = {manv,ten,ngaysinh,diachi,luong,sdt,gioitinh};
            model.addRow(row);
        }
    }
    
    private void BAddNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddNVActionPerformed
         // TODO add your handling code here:
        try {
            if(textManv.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập mã nhân viên!");
            }
            else if(textHoten.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập họ tên nhân viên!");
            }
            else if(textDate.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập ngày sinh nhân viên");
            }
            else if(textDiachi.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập địa chỉ nhân viên!");
            }
            else if(textLuong.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập lương nhân viên!");
            }
            else if(textSodienthoai.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập số điện thoại nhân viên!");
            }
            else{
                DTONhanVien nv = new DTONhanVien();
                nv.setMaNV(textManv.getText());
                nv.setTenNV(textHoten.getText());
                nv.setNgaySinh(textDate.getText());
                nv.setDiaChi(textDiachi.getText());
                nv.setLuong(Float.valueOf(textLuong.getText()));
                nv.setSex((String) ComboBoxGenderNV.getSelectedItem());
                nv.setSDT(textSodienthoai.getText());
                JOptionPane.showMessageDialog(this,BLLnv.BLLthem(nv));
                loadNVList();
                textManv.setText("");textHoten.setText("");textDate.setText("");textDiachi.setText("");textLuong.setText("");textSodienthoai.setText("");
            }  
        } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        } 
    }//GEN-LAST:event_BAddNVActionPerformed

